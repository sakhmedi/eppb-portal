-- ЕППБ — маркер «прочитанных уведомлений» в личном кабинете.
--
-- Уведомления не хранятся отдельной таблицей: они ВЫВОДЯТСЯ из истории смены
-- статусов (applications.status_history). Каждая запись истории с временем changedAt —
-- это событие «изменился статус заявки». Чтобы показывать счётчик непрочитанных,
-- достаточно одной отметки «до какого момента пользователь всё видел».
--
-- «Непрочитано» = запись истории с changedAt > notifications_last_read_at.
-- Пользователь сам обновляет эту отметку (кнопка «Отметить прочитанными») —
-- это покрыто существующей политикой "profiles: update own or admin", а триггер
-- prevent_role_change блокирует лишь смену role, поэтому новых RLS не требуется.

alter table public.users_profiles
  add column if not exists notifications_last_read_at timestamptz;
