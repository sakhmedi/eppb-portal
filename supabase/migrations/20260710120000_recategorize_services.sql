-- ЕППБ — приведение категорий услуг к единому справочнику направлений поддержки.
-- Главная страница показывает 6 направлений (см. lib/catalog-directions.ts), а каталог
-- фильтрует по полю services.category. Чтобы витрина направлений была «живой», категории
-- реальных услуг должны дословно совпадать с ярлыками направлений.
-- Идемпотентно: простые UPDATE по slug, можно применять повторно.

-- Демо-услуга: раньше была общая «Финансовая поддержка» → конкретное направление.
update public.services
set category = 'Субсидирование'
where slug = 'subsidii-msb';

-- Контрольная услуга уже относится к кредитованию — фиксируем явно.
update public.services
set category = 'Кредитование'
where slug = 'agrobiznes-zhivotnovodstvo';
