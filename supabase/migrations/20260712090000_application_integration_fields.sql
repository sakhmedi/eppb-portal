-- ЕППБ — поля интеграций на заявке.
--
-- При финальной подаче заявка проходит через Единую интеграционную шину (демо):
-- подписывается ЭЦП и передаётся во внешнюю BPM-систему дочерней организации, которая
-- возвращает внешний регистрационный номер. Храним эти два факта структурно:
--   external_ref — внешний номер BPM (напр. BPM-2026-000123);
--   signed_at    — момент подписания ЭЦП.
--
-- Оба поля необязательные (у черновиков/старых заявок их нет). Сами события также
-- отражаются в status_history, поэтому UI показывает их и до применения этой миграции.

alter table public.applications
  add column if not exists external_ref text,
  add column if not exists signed_at   timestamptz;
